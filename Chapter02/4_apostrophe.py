# AVOIDING SYNTEX ERRORS USING ""
message = "One of Python's strengths is its diverse community."
print(message)

# AVOIDING SYNTEX ERRORS USING ''
message = 'One of Python\'s strengths is its diverse community.'
print(message)

# AVOIDING SYNTEX ERRORS USING ESCAPE \"
message = "One of Python's strengths is its \"diverse\" community."
print(message)

# AVOIDING SYNTEX ERRORS USING  ""  AND ''
message = "One of Python's strengths is its 'diverse' community."
print(message)

# AVOIDING SYNTEX ERRORS USING  ''  AND ""
message = 'One of Python\'s strengths is its "diverse" community.'
print(message)